#   ① 상품명 '갱신형' → 갱신
#   ② 운전자 → 비갱신
#   ③ 만기 9999(종신) → 비갱신
#   ④ 총회차 > 240 → 비갱신
#   ⑤ 납입년수 == 보장년수 → 갱신 / 다르면 → 비갱신
#   ※ '혼재' 라는 계약 판정은 쓰지 않는다. 계약은 단일 판정.
#     비갱신 계약 안의 (갱신형) 특약은 그 칸만 파랑(13조, 칸>행).
# =====================================================================
def 판정(상품명, 가입년, 만기년, 납입년, 총회차):
    if '갱신형' in 상품명:
        return '갱신', '①상품명 갱신형'
    if str(만기년).startswith('9999') or int(만기년) >= 9999:
        return '비갱신', '③9999(종신)'
    if int(총회차) > 240:
        return '비갱신', f'④총회차 {총회차}>240'
    보장년 = int(만기년) - int(가입년)
    if int(납입년) == 보장년:
        return '갱신', f'⑤납입{납입년}=보장{보장년}'
    return '비갱신', f'⑤납입{납입년}≠보장{보장년}'


def 갱신마커(name):
    """담보명에 갱신 표식이 있으면 True (13조: 칸>행, 그 칸만 파랑)."""
    return ('(H)' in name) or ('갱신형' in name) or ('갱신계약용' in name) or ('(갱신' in name)


