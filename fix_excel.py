def fix(src, dst, edits, who):
    shutil.copy(src, dst)
    wb = openpyxl.load_workbook(dst); ws = wb.active
    rm = rowmap(ws)
    print(f"\n═══ {who} ═══")
    last = ws.max_column
    for name, coff, val, why in edits:
        r = rm.get(name)
        if not r:
            print(f"  ✗ 행없음: {name}"); continue
        assert coff < last, f"합계열 침범: {name}"
        c = coff
        old = ws.cell(r, c).value
        ws.cell(r, c).value = val
        print(f"  {name:22s} C{coff+2} : {str(old):>8} → {str(val):>8}   {why}")
    wb.save(dst)
    print(f"  [확인] 큐 {len(HOLD[who])}건:")
    for h in HOLD[who]:
        print(f"    · {h}")
    return dst

