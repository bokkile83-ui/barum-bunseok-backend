CI_MARK = ['CI','GI','컨버전스','중대한']  # 회사/담보명에 있으면 결합담보=CI태그
def is_ci(nm, 상품=''):
    """CI/GI/컨버전스 결합담보 여부 — True면 일반칸 합산금지, CI 표식 유지."""
    t = nm + ' ' + (상품 or '')
    return any(m in t for m in CI_MARK)

def dambo_name(nm):
    """담보명만 추출 — '담보명_(무)특약명' 구조에서 언더바 앞부분."""
    return nm.split('_')[0].strip()

def classify(nm):
    """반환: (표준행 or None, 사유). 담보명(특약명 제외) 기준. HOLD 우선=추측금지."""
    d = dambo_name(nm)
    for h in HOLD:
        if h in d: return None, f'HOLD:{h}'
    for kw,row in ALIAS:
        if kw in d: return row, kw
    return None, 'UNMAPPED'

